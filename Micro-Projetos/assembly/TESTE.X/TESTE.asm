.ORG   0x0000
    
    LDI R16, 0B11111111
    OUT DDRD, R16
    LDI R16, 0B00000000
    OUT DDRC, R16
    LDI R16, 0B11111111
    OUT PORTC, R16
    CLR R16

    INICIO:
    
	SBIC PINC, PC2
	RJMP INICIO
	RJMP PRESS
    
    PRESS:
	RCALL ATRASO
	SBIS PINC,PC2
	RJMP PRESS
	RJMP SOLTO

    SOLTO:
	RCALL ATRASO
	INC R16
	CPI R16, 16
	BRNE SAIDA
	CLR R16
	RJMP INICIO
	
    SAIDA:
	OUT PORTD, R16
	RJMP INICIO
    
    
    ATRASO:
	DEC R17
	BRNE ATRASO
	DEC R18
	BRNE ATRASO
	RET


